import math

def add(a, b):
    return float(a) + float(b)

def subtract(a, b):
    return float(a) - float(b)
    
def multiply(a, b):
    return float(a) * float(b)

def divide(a, b):
    return float(a) / float(b)
        
def square(a):
    return float(a) ** 2
    
def sqrt(a):
    return math.sqrt(float(a))